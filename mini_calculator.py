a = int(input("A:"))
b = int(input("B:"))
operation = input("add/sub/mul/div")
if (operation == "add"):
    print("The addition of the number is",a+b)
elif (operation == "sub"):
    print("The subtraction of the number is",a-b)
elif (operation == "mul"):
    print("The multiplication of the number is",a*b)
elif (operation == "div"):
    print("The division of the number is:",a/b)
